export const FRENCH_VERB_HELPERS = {
  // Get verbs for specific difficulty level
  getVerbsForDifficulty: (difficulty) => {
    return FRENCH_VERB_DATA.DIFFICULTY_CONFIGS[difficulty]?.verbs || [];
  },

  // Get tenses for specific difficulty level
  getTensesForDifficulty: (difficulty) => {
    return FRENCH_VERB_DATA.DIFFICULTY_CONFIGS[difficulty]?.tenses || [];
  },

  // Get course structure for difficulty level
  getCourseStructure: (difficulty) => {
    return FRENCH_VERB_DATA.DIFFICULTY_CONFIGS[difficulty]?.courseStructure || null;
  },

  // Get conjugation for specific verb, tense, and pronoun
  getConjugation: (verb, tense, pronoun) => {
    return FRENCH_VERB_DATA.VERB_CONJUGATIONS[verb]?.[tense]?.[pronoun] || null;
  },

  // Get question contexts for specific verb
  getQuestionContexts: (verb) => {
    return FRENCH_VERB_DATA.QUESTION_CONTEXTS[verb] || [];
  },

  // Get verb meaning in English
  getVerbMeaning: (verb) => {
    return FRENCH_VERB_DATA.VERB_MEANINGS[verb] || verb;
  },

  // Check if verb is reflexive
  isReflexiveVerb: (verb) => {
    return verb.startsWith('se ') || verb.startsWith("s'");
  },

  // Get all available verbs
  getAllVerbs: () => {
    return Object.keys(FRENCH_VERB_DATA.VERB_CONJUGATIONS);
  },

  // Get all available tenses
  getAllTenses: () => {
    return Object.keys(FRENCH_VERB_DATA.VERB_CONJUGATIONS.être);
  }
};